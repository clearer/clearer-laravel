     * @return \Illuminate\Http\Response
     */
    public function store(Idea $idea)
    {
        //
        $comment = new Comment();
        $comment->text = request('text');
        $comment->idea_id = $idea->id;
        $comment->owner_id = Auth::user()->id;
        $comment->save();

        return redirect()->route('idea.show', compact(['idea']));
    }

